    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        TableType table = new TableType();
        if((jTextField1.getText().equals(""))||(jTextField2.getText().equals(""))||(jTextField3.getText().equals(""))){
            JOptionPane.showMessageDialog(this, "Failed to add item", "Item Processing Status", JOptionPane.INFORMATION_MESSAGE);
        }else{
            table.setCapacity(Integer.parseInt(jTextField1.getText()));
            table.setType(jTextField2.getText());
            table.setReserveNum(Integer.parseInt(jTextField3.getText()));
            String response = addRestaurantTable(shopId, table);
            
            if (response != null) {
                    //tableModel.addRow(new Object[]{response.getId(), jTextFieldItemName.getText(), jTextFieldQuantity.getText(), jTextFieldPrice.getText(), jTextFieldCategory.getText()});
                    this.loadTable();
                    JOptionPane.showMessageDialog(this, response, "Item Processing Status", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(this, response, "Item Processing Status", JOptionPane.INFORMATION_MESSAGE);
                }
                this.setAllToNull();
            this.loadTable();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

