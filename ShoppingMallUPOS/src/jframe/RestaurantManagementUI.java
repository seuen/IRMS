    private void loadTable() {
        tables = getTablesByRestaurantId(Long.parseLong(shopId));
        Object[][] data = new Object[tables.size()][5];
        for (int i = 0; i < tables.size(); i++) {
            data[i][0] = tables.get(i).getId();
            data[i][1] = tables.get(i).getCapacity();
            data[i][2] = tables.get(i).getType();
            data[i][3] = tables.get(i).getReserveNum();
        }

        tableModel = new DefaultTableModel(data, new String[]{"TableId", "Capacity", "Type", "Quantity"});

        jTable1.setModel(tableModel);
        jTable1.getColumnModel().getColumn(0).setMaxWidth(100);
        jTable1.getColumnModel().getColumn(1).setMaxWidth(150);
        jTable1.getColumnModel().getColumn(2).setMaxWidth(100);
        jTable1.getColumnModel().getColumn(3).setMaxWidth(100);        
    }

    private static java.util.List<soap.common.TableType> getTablesByRestaurantId(java.lang.Long restId) {
        soap.common.CommonFunctionsUPOS_Service service = new soap.common.CommonFunctionsUPOS_Service();
        soap.common.CommonFunctionsUPOS port = service.getCommonFunctionsUPOSPort();
        return port.getTablesByRestaurantId(restId);
    }

    private static String addRestaurantTable(java.lang.String restId, soap.common.TableType newTable) {
        soap.common.CommonFunctionsUPOS_Service service = new soap.common.CommonFunctionsUPOS_Service();
        soap.common.CommonFunctionsUPOS port = service.getCommonFunctionsUPOSPort();
        return port.addRestaurantTable(restId, newTable);
    }

    private static Shop getShopById(long shopId) {
        soap.common.CommonFunctionsUPOS_Service service = new soap.common.CommonFunctionsUPOS_Service();
        soap.common.CommonFunctionsUPOS port = service.getCommonFunctionsUPOSPort();
        return port.getShopById(shopId);
    }
